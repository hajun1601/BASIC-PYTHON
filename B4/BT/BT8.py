#Bài 8 - Tính điểm trung bình 3 môn
print("Chương trình tính điểm trung bình 3 môn")
Toan = int(input("Nhập điểm toán : "))
Van = int(input("nhập điểm văn : "))
Anh = int(input("nhập điểm Tiếng Anh : "))
print("")
